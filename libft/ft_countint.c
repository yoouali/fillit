#include "libft.h"

int		ft_countint(int nb)
{
	unsigned int	n;
	int				i;

	if (nb == 0)
		return (1);
	i = 0;
	if (nb < 0)
	{
		n = nb * -1;
		i = 1;
	}
	else
		n = nb;
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
